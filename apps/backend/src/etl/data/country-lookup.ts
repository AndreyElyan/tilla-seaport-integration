const ISO_REGEX = /^[A-Z]{2}$/;

export function resolveCountryIso(value: string): string | undefined {
  const trimmed = value.trim();
  if (ISO_REGEX.test(trimmed.toUpperCase())) {
    return trimmed.toUpperCase();
  }
  return COUNTRY_NAME_TO_ISO[trimmed.toLowerCase()];
}
